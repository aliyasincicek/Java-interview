package javaInterviewQuestions;

public class Java_18_PrintCharcters {

	public static void main(String[] args) {
		// ascii degerleri
		
		for (int i = 'a'; i <='z'; i++) {
			System.out.println(i+" ascii degeri");
			
		
		}
		// alfabe
	for (char ch = 'A'; ch <= 'Z'; ch++) {
		System.out.print(ch);
		
	}
	
	}
	
	

}
